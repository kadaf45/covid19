</h4>
          </div>
          <div class="col-lg-7">
            <p>
              Virus corona baru atau novel coronavirus (nCoV) adalah jenis virus corona baru yang menimbulkan penyakit yang bernama COVID-19. Sebelum dikenal sebagai COVID-19, penyakitnya dikenal sebagai virus corona baru 2019 atau 2019-nCoV.<br><br>

Virus corona baru adalah virus baru, tapi mirip dengan keluarga virus yang menyebabkan SARS (Severe Acute Respiratory Syndrome) dan sejumlah influensa biasa.
            </p>
