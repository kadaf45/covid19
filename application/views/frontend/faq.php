            <h4>Apakah aman memberi ASI pada bayi bila ibu terinfeksi atau suspek virus corona?


</h4>
          </div>
          <div class="col-lg-7">
            <p>
              Semua ibu yang tinggal di wilayah terpengaruh atau memiliki risiko, khususnya yang menunjukkan gejala demam, batuk dan sesak napas harus mencari pengobatan secepat mungkin dan mengikuti perintah tenaga kesehatan.<br><br>

Karena pentingnya pemberian ASI, ibu yang sedang menyusui dapat melanjutkan ASI sambil terus melakukan tindakan-tindakan pencegahan. Sebagaimana virus-virus pernapasan yang lain, virus corona baru tidak menular melalui cairan ASI.<br><br>

Bagi ibu atau siapapun yang mengalami gejala demam, rasa lelah dan batuk kering, yang perlu berinteraksi dengan anak, perhatikan hal-hal berikut.
<ul>
    <li>Gunakan masker saat berada dekat anak (termasuk saat memberi ASI)</li>
    <li>Cuci tangan pakai sabun sebelum dan sesudah kontak dengan anak (termasuk memberi ASI)</li>
    <li>Bersihkan permukaan benda-benda yang tersentuh tangan dengan lap, tisu atau cairan disinfektan.</li>
    <li>Bila ibu terlalu lemah, sebaiknya perah ASI dan berikan pada bayi dengan cangkir atau sendok bersih, sambil tetap mengikuti tindakan-tindakan pencegahan di atas.</li>
