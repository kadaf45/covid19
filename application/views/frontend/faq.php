Terus lakukan perilaku bersih cuci tangan pakai sabun dan pastikan anak telah diimunisasi sehingga anak Anda terlindungi dari virus dan penyakit lain.<br><br>

Hindari tempat umum (tempat kerja, sekolah, transportasi umum) untuk mencegah penyebaran.
            </p>
