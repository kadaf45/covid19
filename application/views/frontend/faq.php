    <li>Mengenakan lebih dari satu masker</li>
    <li>Meminum antibiotik</li>
</ul>

Apa pun keadaannya, jika Anda mengalami demam, batuk dan kesulitan bernapas, segeralah mencari pertolongan medis untuk mengurangi risiko terkena infeksi yang lebih parah dan sampaikan riwayat perjalanan Anda baru-baru ini kepada tenaga kesehatan.<br><br>

sumber: WHO
            </p>
